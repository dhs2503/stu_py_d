#Program to Use Lambda Expressions with map()

numbers = [1, 2, 3, 4]  # Correctly create a list using square brackets
squared = list(map(lambda x: x**2, numbers))  # Correctly use lambda to square each number
print(squared)  # Outputs: [1, 4, 9, 16]

