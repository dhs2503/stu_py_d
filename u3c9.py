#Program to Slice and Access List Elements
my_list = ['a', 'b', 'c', 'd', 'e']
first_element = my_list[0]    # Correctly accessing the first element
last_element = my_list[-1]    # Correctly accessing the last element using negative indexing
sub_list = my_list[1:4]       # Correctly slicing the list from index 1 to 3

print("First element:", first_element)  # Outputs: a
print("Last element:", last_element)    # Outputs: e
print("Sliced list:", sub_list)         # Outputs: ['b', 'c', 'd']

