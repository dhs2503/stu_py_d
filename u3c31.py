#Program to Iterate Through Dictionary Items
my_dict = {'a': 1, 'b': 2, 'c': 3}  # Add the missing closing brace for the dictionary

# Iterate through the dictionary and print key-value pairs
for key, value in my_dict.items():
    print(f"{key}: {value}")  # Close the f-string properly

