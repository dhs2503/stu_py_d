#Python program to demonstrate comparison operators

a = 10
b = 20

#equal to
print(f"{a} == {b}: {a == b}")

#not equal to
print(f"{a} != {b}: {a != b}")

#greater than
print(f"{a} > {b}: {a > b}")

#less than
print(f"{a} < {b}: {a < b}")

#greater than or equal to
print(f"{a} >= {b}: {a >= b}")

#less than or equal to
print(f"{a} <= {b}: {a <= b}")

