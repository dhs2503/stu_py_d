#Program to Use List Comprehension with Condition

numbers = [1, 2, 3, 4, 5]  # Correctly create a list using square brackets
even_numbers = [x for x in numbers if x % 2 == 0]  # List comprehension to filter even numbers
print(even_numbers)  # Outputs: [2, 4]

